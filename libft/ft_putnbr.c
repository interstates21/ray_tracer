#include <unistd.h>

void	ft_putnbr(int n)
{
	long m;
	char digit;

	if (n < 0)
		write(1, "-", 1);
	m = n < 0 ? -(long)n : n;
	if (m > 9)
		ft_putnbr(m / 10);
	digit = (m % 10) + '0';
	write(1, &digit, 1);
}
